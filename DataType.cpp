#include <iostream>

int main() {
	// int is a whole number
	int num = 2;

	// double is a decimal number
	double price = 10.99;

	// char is a single character
	char grade = 'A';

	// bool is a boolean value (true/false)
	bool student = true;
	bool power = false;

	// string is an object that represents a sequence of text
	std::string name = "Bro";
	std::string day = "Friday";
	std::string food = "pizza";

	std::cout << "Hello " << name << '\n';
	std::cout << "I am " << num << " years old." << '\n';

	return 0;
}
